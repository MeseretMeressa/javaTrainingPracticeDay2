public class SortObjects {
	public static void main(String[] args) {
		int [] arr = {11, 22,13,24,15,56,17,18};
		for (int temp : arr){
			System.out.println(temp);
		}
		System.out.println("-------------------------After sort -----");
		Arrays.sort(arr);
		for (int temp : arr){
			System.out.println(temp);
		}
		String[] sarr = {"hello", "how","are", "you"};
		Arrays.sort(sarr);
		for (String temp : sarr){
			System.out.println(temp);
		}
	}
}
